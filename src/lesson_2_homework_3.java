public class lesson_2_homework_3 {
    public static void main(String[] args) {
        int number = -10;
        if (number > 0) {
            number += 1;
        }
        if (number < 0) {
            number -= 2;
        }
        if (number == 0) {
            number = 10;
        }
        System.out.println(number);
    }
}